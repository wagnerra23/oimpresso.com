// fuzzyMatch вЂ” busca local (token AND nos hayfields)
// Port do `kb-page.jsx::fuzzyMatch` вЂ” aplicado em title + excerpt + tags + author
// в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

export function fuzzyMatch(node: KbNode, q: string): boolean {
  if (!q) return true;
  const tags = (node.tags ?? []).join(' ');
  const author = node.author_name ?? '';
  const hay = `${node.title} ${node.excerpt ?? ''} ${tags} ${author}`.toLowerCase();
  return q
    .toLowerCase()
    .split(/\s+/)
    .filter(Boolean)
    .every((part) => hay.includes(part));
}

