// relatedNodes вЂ” top-N relacionados por overlap de tags + categoria + equip
// Port do `kb-extras.jsx::relatedArticles` вЂ” adaptado pra KbNode
// в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

export function relatedNodes(
  node: KbNode | null,
  all: KbNode[],
  n: number = 3,
): KbNode[] {
  if (!node) return [];
  const tagsA = new Set(node.tags ?? []);
  return all
    .filter((b) => b.id !== node.id && b.status !== 'deleted')
    .map((b) => {
      const tagsB = new Set(b.tags ?? []);
      let overlap = 0;
      tagsA.forEach((t) => {
        if (tagsB.has(t)) overlap++;
      });
      const catBonus = b.category_id && b.category_id === node.category_id ? 1.5 : 0;
      const equipBonus =
        node.equip && b.equip === node.equip && b.equip !== 'вЂ”' ? 1 : 0;
      return { node: b, score: overlap * 2 + catBonus + equipBonus };
    })
    .filter((x) => x.score > 0)
    .sort((x, y) => y.score - x.score)
    .slice(0, n)
    .map((x) => x.node);
}

