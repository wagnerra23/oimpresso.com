// Port do `kb-trouble-lib.jsx::kbLinkifyText` вЂ” adaptado pro novo padrГЈo
// в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

import * as React from 'react';

export function kbLinkifyText(
  text: string | null | undefined,
  onPickRef: (ref: string) => void,
): React.ReactNode[] {
  if (!text || typeof text !== 'string') return [text ?? ''];

  const parts: React.ReactNode[] = [];
  // Aceita #kb-NNN (slugs novos), #aNNN ou #tNNN ou #tr-XXX (compat Cowork)
  const re = /#(kb-[\w-]+|a\d+|t\d+|tr-[a-z0-9-]+)/g;
  let last = 0;
  let m: RegExpExecArray | null;
  let i = 0;
  while ((m = re.exec(text)) !== null) {
    if (m.index > last) parts.push(text.slice(last, m.index));
    const id = m[1];
    parts.push(
      React.createElement(
        'button',
        {
          key: `lk-${i++}`,
          className: 'kb-link',
          onClick: (e: React.MouseEvent) => {
            e.stopPropagation();
            onPickRef(id);
          },
        },
        `#${id}`,
      ),
    );
    last = m.index + m[0].length;
  }
  if (last < text.length) parts.push(text.slice(last));
  return parts;
}

