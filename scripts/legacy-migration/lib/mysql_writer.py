    # UPSERT helpers
    # ------------------------------------------------------------------------

    def upsert_account(
        self,
        business_id: int,
        name: str,
        account_number: str,
        legacy_source: str,
        legacy_id: str,
        legacy_metadata: dict | None = None,
        is_closed: bool = False,
        account_type_id: int | None = None,
        created_by: int = 1,
        account_details: str | None = None,
    ) -> int | None:
        """UPSERT em accounts (core) + accounts_legacy_map (bridge).

        Schema real `accounts` (UltimatePOS):
          id, business_id, name, account_number, account_details,
          account_type_id (FK→account_types), note, created_by, is_closed,
          deleted_at, created_at, updated_at

        account_type_id: null por default (Wagner classifica via UI). Pode
        passar id explícito (ex: 1=Banco, 2=Caixa) se conhecer.

        Retorna account_id (real após commit, ou placeholder em dry-run).
        """
        # Step 1: lookup map pra ver se já existe
        existing_id = self._find_account_id_by_legacy(business_id, legacy_source, legacy_id)

        if existing_id is None:
            # INSERT em accounts (schema real UltimatePOS)
            sql_account = """
                INSERT INTO accounts (business_id, name, account_number, account_details, account_type_id, created_by, is_closed, created_at, updated_at)
                VALUES (%(business_id)s, %(name)s, %(account_number)s, %(account_details)s, %(account_type_id)s, %(created_by)s, %(is_closed)s, NOW(), NOW())
            """
            params = {
                "business_id": business_id,
                "name": name,
                "account_number": account_number,
                "account_details": account_details,
                "account_type_id": account_type_id,
                "created_by": created_by,
                "is_closed": 1 if is_closed else 0,
            }
            account_id = self._execute(sql_account, params, returning_id=True)

            # INSERT em accounts_legacy_map
            sql_map = """
                INSERT INTO accounts_legacy_map
                    (business_id, account_id, legacy_source, legacy_id,
                     legacy_imported_at, legacy_importer_version, legacy_metadata,
                     created_at, updated_at)
                VALUES
                    (%(business_id)s, %(account_id)s, %(legacy_source)s, %(legacy_id)s,
                     NOW(), %(importer_version)s, %(legacy_metadata)s,
                     NOW(), NOW())
            """
            self._execute(
                sql_map,
                {
                    "business_id": business_id,
                    "account_id": account_id or 0,
                    "legacy_source": legacy_source,
                    "legacy_id": legacy_id,
                    "importer_version": self.importer_version,
                    "legacy_metadata": json.dumps(legacy_metadata) if legacy_metadata else None,
                },
            )
            self.stats["inserts"] += 1
        else:
            # UPDATE — preserve ID, atualiza fields
            sql_update = """
                UPDATE accounts
                SET name = %(name)s,
                    account_number = %(account_number)s,
                    account_details = %(account_details)s,
                    account_type_id = %(account_type_id)s,
                    is_closed = %(is_closed)s,
                    updated_at = NOW()
                WHERE id = %(account_id)s
            """
            self._execute(
                sql_update,
                {
                    "name": name,
                    "account_number": account_number,
                    "account_details": account_details,
                    "account_type_id": account_type_id,
                    "is_closed": 1 if is_closed else 0,
                    "account_id": existing_id,
                },
            )
            self.stats["updates"] += 1
            account_id = existing_id

        return account_id

