"""
Wrapper MySQL pra escrita idempotente do importer.

3 modos de operação:
  - dry_run: imprime SQL + salva em output/dry-run-<ts>.sql, não executa
  - local:   conecta em oimpresso.test (Herd local Wagner), executa
  - prod:    conecta em Hostinger MySQL via Remote MySQL whitelist (perigoso)

UPSERT idempotente via INSERT ... ON DUPLICATE KEY UPDATE — chave por
(business_id, legacy_source, legacy_id).
"""

from __future__ import annotations

import json
import os
import sys
from datetime import datetime
from pathlib import Path
from typing import Any

try:
    import pymysql
    import pymysql.cursors
except ImportError:
    print(
        "⚠️  pymysql não instalado. Rode: pip install pymysql\n"
        "   (necessário só pra modo --target local|prod; --target dry-run funciona sem)",
        file=sys.stderr,
    )
    pymysql = None  # type: ignore


class MysqlWriter:
    """Encapsula escrita MySQL ou geração SQL pra dry-run."""

    def __init__(
        self,
        target: str = "dry-run",
        host: str = "127.0.0.1",
        port: int = 3306,
        user: str = "root",
        password: str = "",
        database: str = "oimpresso",
        output_dir: Path | None = None,
        importer_version: str = "0.1.0",
    ) -> None:
        self.target = target
        self.host = host
        self.port = port
        self.user = user
        self.password = password
        self.database = database
        self.output_dir = output_dir or Path("output")
        self.importer_version = importer_version

        self.con = None
        self.dry_run_lines: list[str] = []
        self.stats = {"inserts": 0, "updates": 0, "errors": 0}

        self.dry_run_lines.append(f"-- Generated by importer-v{importer_version}")
        self.dry_run_lines.append(f"-- Generated at: {datetime.utcnow().isoformat()}Z")
        self.dry_run_lines.append(f"-- Target: {target}")
        self.dry_run_lines.append("")

    def __enter__(self) -> "MysqlWriter":
        if self.target in ("local", "prod"):
            if pymysql is None:
                raise RuntimeError(
                    "pymysql necessário pra modo --target local|prod"
                )
            self.con = pymysql.connect(
                host=self.host,
                port=self.port,
                user=self.user,
                password=self.password,
                database=self.database,
                charset="utf8mb4",
                cursorclass=pymysql.cursors.DictCursor,
                autocommit=False,
            )
        return self

    def __exit__(self, exc_type, exc_val, exc_tb) -> None:
        if self.target == "dry-run":
            # Salva SQL gerado em arquivo
            self.output_dir.mkdir(parents=True, exist_ok=True)
            ts = datetime.now().strftime("%Y%m%d-%H%M%S")
            path = self.output_dir / f"dry-run-{ts}.sql"
            path.write_text("\n".join(self.dry_run_lines), encoding="utf-8")
            print(f"💾 SQL salvo: {path} ({len(self.dry_run_lines)} linhas)")

        if self.con:
            if exc_type is None:
                self.con.commit()
            else:
                self.con.rollback()
            self.con.close()

